<script>

    const services = JSON.parse('@json($serviceList)');

    function onServiceChange(e) {
        const serviceId = parseInt(e.target.value);
        const service = services.find((s) => s.id === serviceId);

        if(!service) return;

        if(service.type === "NRC") {
            document.getElementById('recurringChargeDates').style.display = "none";
        }
        else {
            document.getElementById('recurringChargeDates').style.display = "block";
        }
    }
</script>
